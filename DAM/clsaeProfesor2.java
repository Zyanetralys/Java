public class Profesores {

  //Atributos

  private String nombre;

  private String curso;

  //Constructor vacio

  public Profesores() {

   this.nombre = "Colegio";

   this.curso = "Historia";

  }

  //Constructor con parámetros

  public Profesores (String nombre, String curso) {

   this.nombre = nombre;

   this.curso = curso;

  }

}
